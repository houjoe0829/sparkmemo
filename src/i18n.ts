/**
 * Resolve Obsidian's configured UI language to one of our supported
 * dictionaries. `getLanguage()` was only added in Obsidian 1.8.7, so we fall
 * back to moment's locale (set from the same setting on older versions) and
 * finally to English.
 */
function detectLocale(): Locale {
  let raw: string;
  try {
    raw = getLanguage();
  } catch {
    raw = moment.locale();
  }
  return raw?.toLowerCase().startsWith('zh') ? 'zh' : 'en';
}

/** Exposes the resolved locale for callers that need to branch on it directly (e.g. number formatting). */
export function currentLocale(): Locale {
  return detectLocale();
}

/**
 * Translate a key for the currently configured Obsidian UI language.
 * Falls back to the English string (or the key itself) if missing.
 * `vars` supports simple `{{name}}` interpolation.
 */
export function t(key: string, vars?: Record<string, string>): string {
  const locale = detectLocale();
  let str = dictionaries[locale][key] ?? dictionaries.en[key] ?? key;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      str = str.replace(`{{${name}}}`, value);
    }
  }
  return str;
}
